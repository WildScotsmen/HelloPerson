/// <summary>
/// An enumeration of gender identities.
/// </summary>
/// <see href="http://www.transstudent.org/definitions/"/>
public enum Gender
{
    /// <summary>The male gender.</summary>
    Male,
    /// <summary>The female gender.</summary>
    Female,
    /// <summary>A blanket term for all people who do not identify as male or female.</summary>
    Nonbinary
}