/// <summary>
/// An enumeration of gender identities.
/// </summary>
/// <see href="https://www.plannedparenthood.org/learn/sexual-orientation-gender/sexual-orientation"/>
public enum SexualOrientation
{
    /// <summary>Attracted to the opposite sex.</summary>
    Straight,
    /// <summary>Attracted to the same sex.</summary>
    Gay,
    /// <summary>Attracted to both sexes.</summary>
    Bisexual,
    /// <summary>Attracted to anyone, regardless of gender identity.</summary>
    Pansexual,
    /// <summary>Does not experience attraction.</summary>
    Asexual,
    /// <summary>Only experiences attraction if a strong bond is present.</summary>
    Demisexual
}